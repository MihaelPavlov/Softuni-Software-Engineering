function whatDayIs(x){
  if (x=='Monday') {
    console.log(1);
  }
  else if(x=='Tuesday'){
    console.log(2);

  }
  else if(x=='Wednesday'){
    console.log(3);

  }
  else if(x=='Thursday'){
    console.log(4);

  }
  else if(x=='Friday'){
    console.log(5);

  }
  else if(x=='Saturday'){
    console.log(6);

  }
  else if(x=='Sunday'){
    console.log(7);

  }
  else{
    console.log('error');
  }
}

whatDayIs("Saturday");
